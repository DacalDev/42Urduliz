#include "libft.h"

char	*ft_strchr(const char *c, int i)
{
	while (*c)
	{
		if (*c == (char)i)
		{
			return ((char *)c);
		}
		c++;
	}
	if (i == '\0')
	{
		return ((char *)c);
	}
	return (0);
}
